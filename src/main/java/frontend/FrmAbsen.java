    public void tampilkanData() {
        String[] kolom = {"NO.ABSEN", "NAMA", "JUMLAH JAM"};
        ArrayList<Absen> list = new Absen().getAll();
        Object rowData[] = new Object[3];

        tblAbsen.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (Absen absn : list) {
            rowData[0] = absn.getNoAbsen();
            rowData[1] = absn.getMahasiswa().getNamaMhs();
            rowData[2] = absn.getJmlhJam();
            ((DefaultTableModel) tblAbsen.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword) {
        String[] kolom = {"NO.ABSEN", "NAMA", "JUMLAH JAM"};
        ArrayList<Absen> list = new Absen().search(keyword);
        Object rowData[] = new Object[3];

        tblAbsen.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (Absen absn : list) {
            rowData[0] = absn.getNoAbsen();
            rowData[1] = absn.getMahasiswa().getNamaMhs();
            rowData[2] = absn.getJmlhJam();

            ((DefaultTableModel) tblAbsen.getModel()).addRow(rowData);
        }
    }
    
    public void tampilkanCmbKategori() {
        cmbMhs.setModel(new DefaultComboBoxModel(new Mahasiswa().getAll().toArray()));
    }

