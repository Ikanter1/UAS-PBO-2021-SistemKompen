    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Absen absn = new Absen();
        absn.setNoAbsen(Integer.parseInt(txtNim.getText()));
        absn.setMahasiswa((Mahasiswa)cmbMhs.getSelectedItem());
        absn.setJmlhJam(Integer.parseInt(txtJmlhJam.getText()));
        absn.save();
        txtNim.setText(Integer.toString(absn.getNoAbsen()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

