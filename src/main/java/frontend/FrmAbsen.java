    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblAbsen.getModel();
        int row = tblAbsen.getSelectedRow();

        Absen absn = new Absen().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        absn.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

