    private void tblAbsenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAbsenMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblAbsen.getModel();
        int row = tblAbsen.getSelectedRow();
        Absen absn = new Absen();
        
        absn = absn.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtNim.setText(String.valueOf(absn.getNoAbsen()));
        cmbMhs.getModel().setSelectedItem(absn.getMahasiswa().getNamaMhs());
        txtJmlhJam.setText(String.valueOf(absn.getJmlhJam()));
    }//GEN-LAST:event_tblAbsenMouseClicked

