    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblKonfirm.getModel();
        int row = tblKonfirm.getSelectedRow();

        Konfirmasi k = new Konfirmasi().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        k.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

