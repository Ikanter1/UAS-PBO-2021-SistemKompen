    public void tampilkanData() {
        String[] kolom = {"ID KONFIRMASI", "NAMA", "TUGAS", "KONFIRM"};
        ArrayList<Konfirmasi> list = new Konfirmasi().getAll();
        Object rowData[] = new Object[4];
        
        tblKonfirm.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getIdkonfirmasi();
            rowData[1] = list.get(i).getMahasiswa().getNamaMhs();
            rowData[2] = list.get(i).getPenugasan().getTugas();
            rowData[3] = list.get(i).getKonfirm();
            ((DefaultTableModel) tblKonfirm.getModel()).addRow(rowData);
        }
    }

    public void cari(String keyword) {
        String[] kolom = {"ID KONFIRMASI", "NAMA", "TUGAS", "KONFIRM"};
        ArrayList<Konfirmasi> list = new Konfirmasi().search(keyword);
        Object rowData[] = new Object[4];
        
        tblKonfirm.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getIdkonfirmasi();
            rowData[1] = list.get(i).getMahasiswa().getNamaMhs();
            rowData[2] = list.get(i).getPenugasan().getTugas();
            rowData[3] = list.get(i).getKonfirm();
            ((DefaultTableModel) tblKonfirm.getModel()).addRow(rowData);
        }
    }
    
    public void tampilkanCmbKategori() {
        cmbTugas.setModel(new DefaultComboBoxModel(new Penugasan().getAll().toArray()));
        cmbNama.setModel(new DefaultComboBoxModel(new Mahasiswa().getAll().toArray()));
    }

