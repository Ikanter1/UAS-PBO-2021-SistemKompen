    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Konfirmasi k = new Konfirmasi();
        k.setIdkonfirmasi(Integer.parseInt(txtId.getText()));
        k.setMahasiswa((Mahasiswa)cmbNama.getSelectedItem());
        k.setPenugasan((Penugasan)cmbTugas.getSelectedItem());
        k.setKonfirm(txtKonfirm.getText());
        k.save();
        
        txtId.setText(Integer.toString(k.getIdkonfirmasi()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

