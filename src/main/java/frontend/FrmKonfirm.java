    private void tblKonfirmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKonfirmMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblKonfirm.getModel();
        int row = tblKonfirm.getSelectedRow();
        Konfirmasi k = new Konfirmasi();
        
        k = k.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        txtId.setText(String.valueOf(k.getIdkonfirmasi()));
        cmbNama.getModel().setSelectedItem(k.getMahasiswa().getNamaMhs());
        cmbTugas.getModel().setSelectedItem(k.getPenugasan().getTugas());
        txtKonfirm.setText(k.getKonfirm());
    }//GEN-LAST:event_tblKonfirmMouseClicked

